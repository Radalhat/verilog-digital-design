# Half Adder

## Overview

A Half Adder is a combinational circuit that adds two binary bits. It produces a **Sum** output and a **Carry** output.

This project implements a **Half Adder** using **Verilog HDL** with the **Dataflow Modelling** technique.

## Inputs

- A
- B

## Outputs

- Sum
- Carry

